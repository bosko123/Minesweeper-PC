package power_up_objects;

public class PlusTime {
	
	private int timeInSec;

	public PlusTime(int timeInSec) {
		super();
		
		this.timeInSec = timeInSec;
		
	}

	public int getTimeInSec() {
		
		return timeInSec;
		
	}

	public void setTimeInSec(int timeInSec) {
		
		this.timeInSec = timeInSec;
		
	}
	
}
